#include<iostream>
using namespace std;
int main()
{
	float r;
	cout<<"This program will print diameter ,area and Circumference."<<endl;
	cout<<"Enter the radius of your circle :: ";
	cin>>r;
	cout<<"Diameter : "<<2*r<<endl;
	cout<<"Area : "<<3.14*r*r<<endl;
	cout<<"Circumference : "<<2*3.14*r;
	return 0;
}
